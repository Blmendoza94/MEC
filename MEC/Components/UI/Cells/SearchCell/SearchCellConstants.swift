    enum SearchTextField {
        static let borderWidth = CGFloat(1)
        static let cornerRadius = CGFloat(4)
        static let topAnchor = CGFloat(16)
        static let leadingAnchor = CGFloat(16)
        static let trailingAnchor = CGFloat(-16)
        static let heightAnchor = CGFloat(50)
    }

    enum SearchButton {
        static let cornerRadius = CGFloat(4)
        static let topAnchor = CGFloat(16)
        static let leadingAnchor = CGFloat(32)
        static let trailingAnchor = CGFloat(-32)
        static let heightAnchor = CGFloat(45)
        static let bottomAnchor = CGFloat(-16)
    }
}
